package day06_tasks;

public class GallonToLiters {
    //Task:
    //add main method
    //write a program that can convert
    //the gallons to litters
    //Ex:
    //gallon = 10;
    //output: 10 gallons equal
    //to 37.8541 liters
    //gallon = 20
    //output: 20 gallons equal
    //to 75.7082 liters
    //---------------------------
    public static void main(String[] args) {
        int gallons = 10;
        double litters = 37.8541;
        System.out.println(gallons + " gallons equal to " +litters +" litters");
        gallons = gallons*2;
        litters = litters*2;
        System.out.println(gallons + " gallons equal to " +litters +" litters");



    }






}
